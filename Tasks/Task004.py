 # Write a Python program to calculate the area of a triangle using the formula
 # Area =1/2 * base * height
 # Take base =10 and height = 5.5

base = float(input("Enter the base of triangle\n"))
print(base)
height = float(input("Enter the height of triangle\n"))
print(height)
area = 1/2 * base * height
print("The area of triangle is: ", area)   # Output is 27.5

