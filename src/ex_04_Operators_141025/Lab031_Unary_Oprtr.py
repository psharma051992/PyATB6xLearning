"""
In below written code, positive value is ignored in Python
Because adding a + sign in front of a number doesn’t change its value
— Python already treats numbers as positive unless told otherwise.
"""
age = +18
age2 = -18
print(age)
print(age2)

how_many_ferrari_Puneet = -1
how_many_ferrari_Puneet = how_many_ferrari_Puneet + 1
print(how_many_ferrari_Puneet)   # 0 as - 1 + 1 = 0
