# Math library
"""
The Math Library (or math module) is a built-in library in Python
that provides mathematical functions and constants — things like:
- Square roots
- Trigonometric functions
- Logarithms
- Powers
- The constants π (pi) and e
"""
import math
print(math.pi)
print(math.pow(2, 3))
print(math.sin(90))
print(math.cos(90))
print(math.tan(90))