# divmod pre-builtin function in python which automatically returns the quotient and remainder

q, r = divmod(5, 3)
print(q)
print(r)

# In Python, we can assign multiple values at the same tine

a, b, c = 1, 2, 3
print(a, b, c) # we can print output in one line too
print(a)
print(b)
print(c)

